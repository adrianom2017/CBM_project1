def OccuranceMatrix(L,c,k):
  occ_matrix={}
  
  for base in ["$","A","C","G","T"]:
    occ=0
    for l in L[:k]:
      if l==base:
        occ+=1
    occ_matrix = {**occ_matrix, **{base : occ}}
  return c,occ_matrix[c]


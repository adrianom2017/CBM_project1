#Burrows-Wheeler Transformation

def BWT(geno):
  geno = geno + "$"
  index = 1
  rotations={1:geno}
  for i in geno:
    geno = geno[1:] + geno[0]
    index += 1 
    rotations={**rotations, **{index : geno}} 
    if geno[0] == "$":
      break
  return rotations

import operator
